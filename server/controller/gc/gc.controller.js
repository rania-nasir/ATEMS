// Helper function to generate passwords
function generateRandomPassword(length) {
  const characters = 'ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789~!@#$%^&*_'; // creating random password from these characters
  let password = '';
  for (let i = 0; i < length; i++) { // randomizing the characters
    const randomIndex = Math.floor(Math.random() * characters.length);
    password += characters[randomIndex];
  }
  return password;
}




const uploadStdData = {
  uploadStd: async (req, res) => {
    const file = req.file;
    if (!file) {
      return res.status(400).json({ error: 'No file uploaded' });
    }
    if (
      file.mimetype !== 'application/vnd.ms-excel' &&
      file.mimetype !== 'application/vnd.openxmlformats-officedocument.spreadsheetml.sheet'
    ) {
      return res.json({ message: 'Invalid file type. Please upload an Excel file' });
    }
    const filePath = file.path;

    try {
      const workbook = xlsx.readFile(filePath);
      const sheet = workbook.Sheets[workbook.SheetNames[0]];
      if (!sheet) {
        return res.json({ message: 'Student data must be in the first excel sheet' });
      }

      const data = xlsx.utils.sheet_to_json(sheet);

      for (const row of data) {
        // Check if required fields are present in the row
        if (!row.rollno || !row.namee || !row.email || !row.gender || !row.batch || !row.semester || !row.program || !row.credithours || !row.cgpa || !row.mobile || !row.thesisstatus || !row.comingevaluation) {
          return res.json({ message: 'Upload correct file with required fields' });
        }
      }

      // Continue with processing the file if all rows have the required fields
      for (const row of data) {
        const existingStudent = await students.findOne({
          where: {
            rollno: row.rollno,
          },
        });
        if (!existingStudent) {
          const autoGeneratedPassword = generateRandomPassword(8);
          const studentData = {
            rollno: row.rollno,
            name: row.namee,
            email: row.email,
            gender: row.gender,
            batch: row.batch,
            semester: row.semester,
            program: row.program,
            credithours: row.credithours,
            cgpa: row.cgpa,
            mobile: row.mobile,
            thesisstatus: row.thesisstatus,
            comingevaluation: row.comingevaluation,
            password: autoGeneratedPassword,
            createdAt: new Date(),
            updatedAt: new Date(),
          };
          await students.create(studentData);
          const toEmail = row.email;
          const subject = 'Welcome to ATEMS';
          const text = `Your credentials to login to the system are as follows:
            Username: ${row.rollno}
            Password: ${autoGeneratedPassword}`;
          sendMail(toEmail, subject, text);
        }
      };

      res.json({ message: 'File uploaded successfully' });
    } catch (error) {
      console.error('Error uploading file:', error);
      res.json({ message: 'Error uploading file' });
    }
  },
};


